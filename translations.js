// Syst√®me de gestion des langues
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'fr';
        this.init();
    }

    init() {
        this.updateLanguage(this.currentLanguage);
        this.bindEvents();
    }

    bindEvents() {
        // Gestion des boutons de langue
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const lang = e.target.getAttribute('data-lang');
                this.updateLanguage(lang);
            });
        });
    }

    updateLanguage(lang) {
        if (!translations[lang]) return;

        this.currentLanguage = lang;
        localStorage.setItem('language', lang);

        // Mettre √† jour l'attribut lang du document
        document.documentElement.setAttribute('lang', lang);
        
        // Mettre √† jour la direction pour l'arabe
        document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

        // Mettre √† jour les classes CSS pour la langue
        document.body.className = document.body.className.replace(/lang-\w+/g, '');
        document.body.classList.add(`lang-${lang}`);

        // Mettre √† jour tous les textes
        this.updateTexts(lang);

        // Mettre √† jour les boutons de langue
        this.updateLanguageButtons(lang);

        // Mettre √† jour les meta tags
        this.updateMetaTags(lang);
    }

    updateTexts(lang) {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getNestedTranslation(translations[lang], key);
            
            if (translation) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                } else if (element.hasAttribute('content')) {
                    element.setAttribute('content', translation);
                } else {
                    element.innerHTML = translation;
                }
            }
        });
    }

    updateLanguageButtons(lang) {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            }
        });
    }

    updateMetaTags(lang) {
        const title = document.querySelector('title');
        const description = document.querySelector('meta[name="description"]');
        
        if (title) {
            title.textContent = translations[lang].meta.title;
        }
        
        if (description) {
            description.setAttribute('content', translations[lang].meta.description);
        }
    }

    getNestedTranslation(obj, path) {
        return path.split('.').reduce((current, key) => {
            return current && current[key] ? current[key] : null;
        }, obj);
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }

    // M√©thode pour obtenir une traduction sp√©cifique
    t(key) {
        return this.getNestedTranslation(translations[this.currentLanguage], key) || key;
    }
}

